class Employee{

   setEmployee(id, name, email, phone){
    this.id = id;
    this.name = name;
    this,email = email;
    this.phone = phone;
   }

   getName(){
    return this.name;
   }

   getId(){
    return this.id;
   }

   getEmail(){
    return this.email;
   }

   getPhone(){
    return this.phone;
   }


}